// There are 5 primitive types in JS (ES5):

// undefined: represents lack of existence.
var undefinedVariable;

// null: represents lack of existence BUT it is a valid value.
var nullified = null;

// boolean: true or false.
var fiftyfifty = true;

// number: the ONLY numeric type. always a floating point in JS.
var num = 43;

// string: a sequence of characters.
var str = 'super slinky' || "nickle wound";
